    public void collec_display(int val,String fid,int upperlimit) {
    try {
        String hql;
        Session session = HibernateUtil.getSessionFactory().openSession();
        session.beginTransaction();
        switch(val){
            case 0:
                hql="from DtFarmerAccount c order by c.id desc";
                break;  
                        
            case 1:
                hql="from DtFarmerAccount c WHERE c.farmerId=:id  order by id desc";
                break;
            default:
                hql="from DtFarmerAccount c order by id desc";
                break;        
    }
        Query q = session.createQuery(hql);      
        if(val==1)
            q.setParameter("id", fid);
        q.setFirstResult(upperlimit);
        q.setMaxResults(30);
        List resultList = q.list();
        displayResult(resultList);
        session.getTransaction().commit();
    } catch (HibernateException he) {
        he.printStackTrace();
    }
}
    
    private void displayResult(List resultList) {
    Vector tableData = new Vector();
//    total=0.0;
    String header[]={"id","farmerId","Name","Date","Type","Credit_on","Debit","Credit","Attendant","",""};
    Vector<String> tableHeaders= new Vector<>();
    tableHeaders.addAll(Arrays.asList(header));
    for(Object o : resultList) {
        
        DtFarmerAccount collection = (DtFarmerAccount)o;
        Vector<Object> oneRow = new Vector<>();
        List col=Arrays.asList(collection.getId(),
                               collection.getFarmerId(),
                               sup.Farmer_name(collection.getFarmerId()),
                               collection.getDate(),
                               collection.getType(),
                               collection.getDescription(),
                               collection.getDebit(),
                               collection.getCredit(),
                               collection.getAttendantId(),
                               "edit",
                               "delete");
        oneRow.addAll(col);
        tableData.add(oneRow);
    }
    if(resultList.isEmpty()){
       farmer_model=new DefaultTableModel(new String[20][11],header);   
    }else{
        farmer_model=new DefaultTableModel(tableData, tableHeaders);
//        jLabel17.setText(String.valueOf(total));
    }
}
    
    public boolean farmer_acc(String farmerId, Date date, String type, String description, double debit, double credit, String attendantId){
      Session session = HibernateUtil.getSessionFactory().openSession();      
      Transaction tx = null;
      boolean state=false;
      try{
          
            DtFarmerAccount acc=new DtFarmerAccount(farmerId, date, type, description, debit, credit, attendantId);
            tx = session.beginTransaction();
            Query q = session.createQuery("FROM DtFarmerAccount acc WHERE acc.farmerId=:id and acc.type=:type and acc.date=:date ");
            q.setParameter("id", farmerId);
            q.setParameter("type", type);
            q.setParameter("date", date);
            List results=q.list();
            Iterator iterator=results.iterator();         
         if(!iterator.hasNext())
             session.save(acc);
         else{
             acc=(DtFarmerAccount)results.get(0);
             acc.setFarmerId(farmerId);
             acc.setDescription(description);
             acc.setDebit(debit);
             acc.setCredit(credit);
             acc.setAttendantId(attendantId);
             session.update(acc);  
         }
            tx.commit();
            state=true;
            
      }catch (HibernateException e) {
         if (tx!=null) tx.rollback();
      }finally {
         session.flush();
         session.close(); 
      }
      return state;
   }
    
