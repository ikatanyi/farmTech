    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int col=jTable1.getSelectedColumn();
        //        String date=new SimpleDateFormat("dd-MM-yyyy").format(jDateChooser1.getDate());
        int row=jTable1.getSelectedRow();
        String farmerid=sup.getValue(jTable1.getValueAt(row, 1));
        String type=sup.getValue(jTable1.getValueAt(row, 4));
        String date=sup.getValue(jTable1.getValueAt(row, 3));
        String credit_on=sup.getIntValue(jTable1.getValueAt(row, 5));        
        int id=Integer.parseInt(sup.getIntValue(jTable1.getValueAt(row, 0)));
        if(col==9){
            jCheckBox1.setSelected(false);
            jComboBox2.setSelectedItem(type);
            txtquantity.setText(sup.getIntValue(jTable1.getValueAt(row, 7)));
            cmbtype.setSelectedItem(credit_on);
            jDateChooser1.setDate(sup.StringToJDate(sup.getValue(date)));
            fillFields(farmer.FarmerID_Search(farmerid));
        }
        if(col==10){
            errorFields = new JLabel("<HTML><FONT COLOR = red>Are you sure you want Delete This Record!</FONT></HTML>");
            int  result = JOptionPane.showInternalConfirmDialog(this,errorFields,"Dairy Management System",JOptionPane.OK_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE );
            if(result==JOptionPane.OK_OPTION){
                this.delete(id);     
                DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
                model.removeRow(row);
              }
         }
    }//GEN-LAST:event_jTable1MouseClicked

