    private DefaultComboBoxModel factories () {                 
       int i=0;
       DefaultComboBoxModel tmodel =new DefaultComboBoxModel();
       org.hibernate.Session session = HibernateUtil.getSessionFactory().openSession();
      try {        
           session.beginTransaction();
           Query q = session.createQuery("from DtFactory");
           List auth=q.list();
           Iterator iterator=auth.iterator();
           id=new Object[auth.size()];
           while(iterator.hasNext()){ 
                   DtFactory factory=(DtFactory)iterator.next();
                   tmodel.addElement(factory.getCompanyName()); 
                   id[i]=factory.getId();
                   i++;         
           }
           session.getTransaction().commit();
         }catch (HibernateException he) {
                 errorFields = new JLabel("<HTML><FONT COLOR = Blue>'"+he.getStackTrace()+"'</FONT></HTML>");
                 JOptionPane.showMessageDialog(null,errorFields,"DMS",JOptionPane.ERROR_MESSAGE);

         }finally {
             session.close(); 
         }
          return tmodel;
    }
    
    
    public void factorySearch(int id ){
       Session session = HibernateUtil.getSessionFactory().openSession();
       Transaction tx = null;
      try{
         tx = session.beginTransaction();
          factory= (DtFactory)session.get(DtFactory.class, id);
         if(factory!=null){
            rate=factory.getRate();
         }
         tx.commit();
      }catch (HibernateException e) {
         if (tx!=null) tx.rollback();
               e.printStackTrace(); 
      }finally {
         session.close(); 
      }
   }
     
     
     public boolean milk_sales(int customerId, double qty, double rate,double debit, double credit, String date, String userid, String descriprion){
      Session session = HibernateUtil.getSessionFactory().openSession();      
      Transaction tx = null;
      int val=0;
      boolean state=false;
      try{ 
            tx = session.beginTransaction();         
             
            DtSales sales=new DtSales(customerId,dept, qty,rate,debit, credit, sup.sqlDate(date), userid);
            DtMstock stock=new DtMstock(0, sup.sqlDate(date), descriprion, customerId, 0.0, qty, userid);
            session.save(stock);
            //save collection
            sales.setSale_stock(stock);
            session.save(sales);
            tx.commit();
            state=true;            
      }catch (HibernateException e) {
         if (tx!=null) tx.rollback();
             e.printStackTrace(); 
      }finally {
         session.flush();
         session.close(); 
      }
      return state;
   }   
    
    private String stock_availability () {                 
       int i=0;
       String avail="0.0";
       org.hibernate.Session session = HibernateUtil.getSessionFactory().openSession();
      try {        
           
           session.beginTransaction();
           Query q = session.createQuery("SELECT sum(stock.instock), SUM(stock.outstock) from VMstock stock ");
           List auth=q.list();
           Iterator iterator=auth.iterator();
           if(iterator.hasNext()){ 
                   Object stock[]=(Object[])iterator.next();
                   if(stock.length!=0){
                       double available=Double.parseDouble(sup.getCostValue(stock[0]))-Double.parseDouble(sup.getCostValue(stock[1]));
                       if(available!=0.0){
                          avail=String.valueOf(available);
                       }        
                   }
           }
           session.getTransaction().commit();
         }catch ( HibernateException | NumberFormatException he) {
                 errorFields = new JLabel("<HTML><FONT COLOR = Blue>'"+he.getMessage()+"'</FONT></HTML>");
                 JOptionPane.showMessageDialog(null,errorFields,"DMS",JOptionPane.ERROR_MESSAGE);

         }finally {
             session.close(); 
         }
          return avail;
    }
    
