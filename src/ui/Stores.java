   private void inventory_display(int val,String name, int upperlimit) {
    try {
        String hql;
        Session session = HibernateUtil.getSessionFactory().openSession();
        session.beginTransaction();
        switch(val){
            case 0:
                hql="from DtInventory invent order by invent.id desc";
                break;
            case 1:
                hql="from DtInventory invent WHERE invent.itemName  LIKE :name order by invent.id desc";
                break;  
            case 2:
                hql="from DtInventory invent WHERE invent.itemName=:name order by invent.id desc";
                break;
            case 3:
                hql="from DtInventory invent order by invent.id desc";
                break;
            default:
                hql="from DteInventory invent order by invent.id desc";
                break;
    }
        Query q = session.createQuery(hql);
        if(val==1)
           q.setParameter("name", "%"+name+"%");
        if(val==2)
             q.setParameter("name", name);
        q.setFirstResult(upperlimit);
        q.setCacheable(true);
        q.setMaxResults(30);
        List resultList = q.list();
        if(val==2||val==3)
           displayItems(resultList);
        else
            displayInventory(resultList);
        session.getTransaction().commit();
    } catch (HibernateException he) {
        errorFields = new JLabel("<HTML><FONT COLOR = BLUE>'"+he.getMessage()+"'</FONT></HTML>");
        JOptionPane.showMessageDialog(null,errorFields,"farmTech",JOptionPane.ERROR_MESSAGE); 
    }
   }
   
