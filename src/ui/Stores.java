    public boolean stockTrx(String productname,int unit,String date){           
    org.hibernate.Session session = HibernateUtil.getSessionFactory().openSession();
    boolean state=false;
    try {        
       session.beginTransaction();
       Query q = session.createQuery("from VStoreBatchStock stock where stock.availableStock>0 AND stock.productName=:product order by stock.batchno ASC");
       q.setParameter("product", productname);
       q.setCacheable(true);
       List auth=q.list();
       Iterator iterator=auth.iterator();
       while(iterator.hasNext()){ 
            VStoreBatchStock batchstock=(VStoreBatchStock)iterator.next(); 
            int available=batchstock.getAvailableStock();
            int stockvalue=0;
            if(available!=0 && unit!=0){
               if(available>=unit){                       
                  stockvalue=unit;
                  unit=0;
               }else{
                   unit=unit-available;
                   stockvalue=available;
               }  
               String batchno=batchstock.getBatchno();
               if(stock_trx(productname, batchno, 0, stockvalue, date)){
                   state=true;
               }
           }   
       }
       session.getTransaction().commit();
     }catch (HibernateException he) {
             errorFields = new JLabel("<HTML><FONT COLOR = Blue>'"+he.getMessage()+"'</FONT></HTML>");
             JOptionPane.showMessageDialog(null,errorFields,"farmTech",JOptionPane.ERROR_MESSAGE);

     }finally {
         session.close(); 
     }
      return state;
    }
    
