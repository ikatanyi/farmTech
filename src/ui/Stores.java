        Query q = session.createQuery(hql);
        q.setCacheable(true);
        List result = q.list();     
        productCode=new String[result.size()+1];
        description=new String[result.size()+1];
        sellingprice=new Double[result.size()+1];
        buyingprice=new Double[result.size()+1];
