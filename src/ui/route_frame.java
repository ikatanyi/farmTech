    public void routeSearch(int supplierid ){
       Session session = HibernateUtil.getSessionFactory().openSession();
       Transaction tx = null;
      try{
         tx = session.beginTransaction();
          routes= (DtRoute)session.get(DtRoute.class, supplierid);
         if(routes!=null){
            setFields();          
         }else
             resetFields();
         tx.commit();
      }catch (HibernateException e) {
         if (tx!=null) tx.rollback();
            e.printStackTrace(); 
      }finally {
         session.close(); 
      }
   }
    
    public boolean add_route(String name,String centerNo, String agentFname, String agentLname, String vehicleNo, String agentPhone, String agentAddress, String agentEmail, String leaderLname, String leaderFname, String leaderPhone, String leaderAddress, String leaderEmail, int memberNo, String altFname, String altLname, String altPhone, String altAddress, String userid){
      Session session = HibernateUtil.getSessionFactory().openSession();      
      Transaction tx = null;
      int val=0;
      boolean state=false;
      try{          
            DtRoute routs=new DtRoute(name,centerNo, agentFname, agentLname, vehicleNo, agentPhone, agentAddress, agentEmail, leaderLname, leaderFname, leaderPhone, leaderAddress, leaderEmail, memberNo, altFname, altLname, altPhone, altAddress, userid);
           
            tx = session.beginTransaction();
            Query q = session.createQuery("FROM DtRoute route WHERE route.name=:name");
            q.setParameter("name", name);
            List results=q.list();
            Iterator iterator=results.iterator();         
         if(!iterator.hasNext()){
            session.save(routs);
            val=1;}
         else{
           DtRoute  rout=(DtRoute)results.get(0);             
           rout.setCenterNo(centerNo);
           rout.getAgentFname();
           rout.setAgentLname(agentLname);
           rout.setVehicleNo(vehicleNo);
           rout.setAgentPhone(agentPhone);
           rout.setAgentAddress(agentAddress);
           rout.setAgentEmail(agentEmail);
           rout.setLeaderFname(leaderFname);
           rout.setLeaderLname(leaderLname);
           rout.setLeaderAddress(leaderAddress);
           rout.setLeaderEmail(leaderEmail);
           rout.setAltFname(altFname);
           rout.setAltLname(altLname);
           rout.setAltPhone(altPhone);
           rout.setAltAddress(altAddress);
           rout.setUserid(userid);
//             routs.setId(rout.getId());
             session.saveOrUpdate(routs);  
             val=2;
         }
            tx.commit();
            if(val==1){
                errorFields = new JLabel("<HTML><FONT COLOR = BLUE>Record saved successfully.</FONT></HTML>");
                JOptionPane.showMessageDialog(null,errorFields,"DMS management system",JOptionPane.INFORMATION_MESSAGE);
            }if(val==2){
                errorFields = new JLabel("<HTML><FONT COLOR = BLUE>Record updated successfully.</FONT></HTML>");
               JOptionPane.showMessageDialog(null,errorFields,"DMS management system",JOptionPane.INFORMATION_MESSAGE);
            }
            state=true;
            
      }catch (HibernateException e) {
         if (tx!=null) tx.rollback();
             e.printStackTrace(); 
      }finally {
         session.flush();
         session.close(); 
      }
      return state;
   }
    
    private boolean deleteRoute(int id){
       org.hibernate.Session session = HibernateUtil.getSessionFactory().openSession();
       Transaction tx = null;
       boolean status=false;
      try{
         tx = session.beginTransaction();
         DtRoute rout = (DtRoute)session.get(DtRoute.class, id); 
         if(rout!=null)
            session.delete(rout);
         tx.commit();
         status=true;
      }catch (HibernateException e) {
         if (tx!=null) tx.rollback();
             errorFields = new JLabel("<HTML><FONT COLOR = BLUE>'"+e.getMessage()+"'</FONT></HTML>");
             JOptionPane.showMessageDialog(null,errorFields,"DMS",JOptionPane.ERROR_MESSAGE); 
      }finally {
         session.close(); 
      }
     return status;
   }

    
