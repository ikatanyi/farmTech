    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String name=sup.getValue(jComboBox1.getSelectedItem());
        String agentFname=txtagentfname.getText(); 
        String agentLname=txtagent_lname.getText();
        String vehicleNo=sup.getValue(cmbvehicle.getSelectedItem());
        String agentPhone=txtagent_phone.getText(); 
        String agentAddress=txtagenta_ddress.getText();
        String agentEmail=txtagent_email.getText(); 
        String leaderLname=txtllname.getText();
        String leaderFname=txt_lfname.getText();
        String leaderPhone=txtlphone.getText(); 
        String leaderAddress=txtladdress.getText();
        String leaderEmail=txtlemail.getText();
        int memberNo=Integer.parseInt(new superCls().getIntValue(txtmembers.getText())); 
        String altFname=txt_altfname.getText(); 
        String altLname=txt_altlname.getText(); 
        String altPhone=txt_altphone.getText(); 
        String altAddress=txt_altemail.getText();
        String centerNo=txtno.getText();
        String userid= this.userid;
        
        if(!name.equals("") &&!leaderPhone.equals("")&& !centerNo.equals("")){
            this.add_route(name,centerNo, agentFname, agentLname, vehicleNo, agentPhone, agentAddress, agentEmail, leaderLname, leaderFname, leaderPhone, leaderAddress, leaderEmail, memberNo, altFname, altLname, altPhone, altAddress, userid);
            jComboBox1.setModel(routes());
            cmbvehicle.setModel(transport());
        }else{
             errorFields = new JLabel("<HTML><FONT COLOR = Blue>Please make sure required fields are filled</FONT></HTML>");
             JOptionPane.showMessageDialog(null,errorFields,"DMS",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

