    private void payroll_display(int val,String farmerid,String sesion, int year,int upperlimit) {
    try {
        String hql;
        Session session = HibernateUtil.getSessionFactory().openSession();
        session.beginTransaction();
        switch(val){
            case 0:
                hql="from VCoffeeFarmerPayroll pay where pay.session=:session and Year(pay.date)=:year order by pay.id asc";
                break;
            case 1:
                hql="from VCoffeeFarmerPayroll pay where pay.farmerId=:id and pay.session=:session and Year(pay.date)=:year order by pay.id asc";
                break;
            case 2:
                hql="from VCoffeeFarmerPayroll pay where pay.session=:session and Year(pay.date)=:year order by pay.id asc";
                break;
            case 3:
                hql="from VCoffeeFarmerPayroll pay where pay.session=:session and Year(pay.date)=:year and pay.type= order by pay.id asc";
                break;
            default:
                hql="from VCoffeeFarmerPayroll pay where pay.session=:session and Year(pay.date)=:year order by pay.id asc";
                break;
    }
        Query q = session.createQuery(hql);
        if(val==1)
            q.setParameter("id", farmerid);
        
         q.setParameter("session", sesion);
         q.setParameter("year", year); 
         q.setFirstResult(upperlimit);
         q.setMaxResults(30);
         List resultList = q.list();
         displayAssets(resultList);        
         session.getTransaction().commit();
    } catch (HibernateException he) {
        errorFields = new JLabel("<HTML><FONT COLOR = BLUE>'"+he.getMessage()+"'</FONT></HTML>");
        JOptionPane.showMessageDialog(null,errorFields,"DMS_fpayment",JOptionPane.ERROR_MESSAGE); 
    }
   }
   
   private void displayAssets(List resultList) {
    Vector tableData = new Vector();
    double gpay=0.0,credit=0.0,net=0.0;
    String header[]={"farmerId","Name","Date","Quantity","Session","rate","Gross","Credit","Net",""};
    Vector<String> tableHeaders= new Vector<>();
    Vector<Object> oneRow =null;
    tableHeaders.addAll(Arrays.asList(header));
    Object[] emp=new Object[10];
    for(Object o : resultList) {        
        VCoffeeFarmerPayroll collection = (VCoffeeFarmerPayroll)o;
        oneRow = new Vector<>();
        List col=Arrays.asList(
                               collection.getFarmerId(),
                               sup.Farmer_name(collection.getFarmerId()),
                               collection.getDate(),
                               collection.getQuantity(),
                               collection.getSession(),
                               collection.getRate(),
                               collection.getGrossPay(),
                               collection.getCredit(),
                               collection.getNetTotal(),
                               "Gen.Payslip"
                               );
         gpay+=collection.getGrossPay();
         credit+=collection.getCredit();
         net+=collection.getNetTotal();
                         
        oneRow.addAll(col);
        tableData.add(oneRow);
    }
    oneRow = new Vector<>();
    Object[] emp1={"","","","Total","","",gpay,credit,net,""};
    oneRow.addAll(Arrays.asList(emp));
    tableData.add(oneRow);
    oneRow = new Vector<>();
    oneRow.addAll(Arrays.asList(emp1));
    tableData.add(oneRow);
    if(resultList.isEmpty()){
       farmer_model=new DefaultTableModel(new String[9][20],header);   
    }else
        farmer_model=new DefaultTableModel(tableData, tableHeaders);
   }
