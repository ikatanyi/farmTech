    private DefaultComboBoxModel getFarmers () {                 
     int i=0;
     DefaultComboBoxModel tmodel =new DefaultComboBoxModel();
     org.hibernate.Session session = HibernateUtil.getSessionFactory().openSession();
     try {        
           session.beginTransaction();
           Query q = session.createQuery("SELECT CONCAT(f.fname,' ',f.lname),f.farmerId FROM DtFarmerdetails f order by cast(f.farmerId as integer ) ASC");
           List auth=q.list();
           farmid=new Object[auth.size()+1];
           Iterator iterator=auth.iterator();
           tmodel.addElement(""); 
           farmid[i]="";
           i++;
           while(iterator.hasNext()){
               Object[] val=(Object[])iterator.next();
                   tmodel.addElement(val[1]+"-"+val[0]);  
                   farmid[i]=val[1];
                   i++;         
       }
       session.getTransaction().commit();
      }catch (HibernateException he) {
             errorFields = new JLabel("<HTML><FONT COLOR = Blue>'"+he.getMessage()+"'</FONT></HTML>");
             JOptionPane.showMessageDialog(null,errorFields,"farmTech",JOptionPane.ERROR_MESSAGE);

      }finally {
         session.close(); 
      }
       return tmodel;
     }
