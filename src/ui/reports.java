        final HashMap jasperparameter=new HashMap();
        int val=cmbfarm.getSelectedIndex();
        if(val!=-1){
            farm_no=new superCls().getValue(farmid[val]);
            this.setFarm_no(farm_no);
        }
        this.setYear(jYearChooser1.getYear());
        String reportname="rpt_quarterly_1.jasper";
        
            if(jRadioButton1.isSelected())
                reportname="rpt_quarterly_1.jasper";
            if(jRadioButton2.isSelected())
                reportname="rpt_quarterly_2.jasper";
            if(jRadioButton3.isSelected())
                reportname="rpt_quarterly_3.jasper";
            if(jRadioButton4.isSelected())
                reportname="rpt_quarterly_4.jasper";
            if(jRadioButton5.isSelected())
                reportname="rpt_first_quarter.jasper";
            jasperparameter.put("year", this.getYear());
            jasperparameter.put("farmerid", farm_no);            
            final String report=reportname;
            rpt=new clsReport();
            jLabel3.setVisible(true);
            jLabel4.setVisible(true);
            jProgressBar1.setVisible(true);
            SwingWorker doComputeRate=new SwingWorker() {
                      @Override
                      protected Integer doInBackground() throws Exception{
                         rpt.report_viewer_ws(report, jasperparameter);
                      return 0;
                     }
                     @Override
                    protected void done(){
                        jLabel3.setVisible(false);
                        jLabel4.setVisible(false);
                        jProgressBar1.setVisible(false);
                    }
                    };
                     doComputeRate.execute();
         
