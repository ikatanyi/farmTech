private void farmer_display(int val,int table,String name,int upperlimit) {
    Session session = HibernateUtil.getSessionFactory().openSession();
    try {
        String hql;
        session.beginTransaction();
        switch(val){
            case 0:
                hql="from DtFarmerdetails F order by fname asc";
                break;
            case 1:
                hql="from DtFarmerdetails F WHERE CONCAT_ws(' ',F.fname,F.lname) LIKE '%"+name+"%' order by fname asc";
                break;
            case 2:
                hql="from DtFarmerdetails F WHERE F.PPhone='"+name+"' order by fname asc";
                break;
            case 3:
                hql="from DtFarmerdetails F WHERE F.farmerId='"+name+"' order by fname asc";
                break;
            case 4:
                hql="from DtFarmerdetails F WHERE F.mno='' order by fname asc";
                break;
            case 5:
                 hql="from DtFarmerdetails F WHERE F.mno<>'' order by fname asc";
                break;
            default:
                hql="from DtFarmerdetails order by fname asc";
                break;        
    }
        Query q = session.createQuery(hql);
        q.setFirstResult(upperlimit);
        q.setMaxResults(30);
        List resultList = q.list();
        displayResult(resultList);
        session.getTransaction().commit();
    } catch (HibernateException he) {
             he.printStackTrace();
    }
    finally{
        session.close();
    }
            
}
    
